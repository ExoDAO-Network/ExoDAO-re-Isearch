/* The following function is index time and lets one define what kinds of terms are NOT acceptable
  for the index

  _ib_IsExcludedIndexTerm affirms that a term is to be excluded.

   Args:
    ptr     := pointer to the start of the term (C-string, nul terminated)
    len     := length (derived from strlen). If 0 then use strlen as words always have
               a length and 0 means that the length was not yet caculated.

  returns:
    1       := Don't index this word
    0       := Add the term to the index

NOTE: The words called are in tail-to-head sorted order--- as-if one was to take a sorted
list and connect the last word to the first to make a ring. 

*/
int (* const _ib_IsExcludedIndexTerm)  (const char *word, unsigned int len) = 0;

/* The following functions below are BOTH search and index time and lets one define what kinds of terms
   are acceptable, including single character etc.

    _ib_IsSearchableTerm()  works only to affirm (positive). Terms that return 0 can still
   be defined as searchable (resp. indexable). 

   Args:
    fieldname := the name (path) of the field (during indexing this is ALWAYS a NULL pointer) 
    ptr       := pointer to the character string
    len       := the length of the word

   returns:
    1  := Is OK to search for
    0  := Don't know if its OK.

   Background: This function allows for a more sophisticated approach to "stop words" including
   field specific approaches.

*/
int (* const _ib_IsSearchableTerm) (const char *fieldname, const unsigned char *ptr, unsigned int len) = 0;

/* as above but returns:
   1  := Not OK to search for resp. index
   0  := Don't know if its OK
*/
int (* const _ib_IsExcludedSearchTerm) (const char *fieldname, const unsigned char *ptr, unsigned int len) = 0;


/* The below **can** be set to point to a function to define what is a term character during indexing.
   It should NOT be defined or used UNLESS the desired functionality CANNOT be defined by use of _ib_isdot()
   and/or a modification of the ctype tables below. 

   Arg: const unsigned char *buffer

   The function should return 0 if the character buffer[0] is NOT a term character (1 otherwise).
   Example:
      _ib_IsTermChr("hold") asks if 'h' in "hold" is a term character? If I know its not then 0.
      If I think it might be then 1. 
   Returning 1 does not mean that its definitely a term character only that we don't know.
   Returning 0 means that we know definitely that its NOT a term character.

   It is defined as a pointer to allow routines to look at preceeding and following characters
   to implement models more generic than even possible with _ib_isdot().

   A need for this function is atypical and a function should only be defined IF and ONLY IF there
   is absolutely no other way using the "standard" means (_ib_isdot() and the ctype tables) to
   define the needed behaviour.

   NOTE: Defining this function WILL (slightly) negatively impact indexing performance.
*/
int (* const _ib_IsTermChr) (const unsigned char *) = 0;


/* _ib_isdot() is used to specify what characters are "special" and if preceeded by a term character
  and followed by an alpha character are to be handled as part of a word */ 
int _ib_isdot(const int ch)
{
#if (DOT_WORDS_SIGNATURE == 8)
  extern int ispunct(int c);
# define _IST_DOT_IN_WORD(_x) ( ispunct(_x) )
#elif (DOT_WORDS_SIGNATURE == 7)
  /* Nothing: No dots in words */
# define _IS_DOT_IN_WORD(_x) 0
#elif (DOT_WORDS_SIGNATURE == 6)
# define _IS_DOT_IN_WORD(_x) ((_x) == '.' || (_x) == ';' || (_x) == ':' || (_x) == '+')
#elif (DOT_WORDS_SIGNATURE == 5)
  // AT&T edz@nonmonotonic.com Zimmermann_edward 1.B auto-mobile and URLs are all words
# define _IS_DOT_IN_WORD(_x) ((_x) == '.' || (_x) == '_' || (_x) == '&' || (_x) == '@' || \
	(_x) == '/' || (_x) = '-')
#elif (DOT_WORDS_SIGNATURE == 4) 
  /* AT&T edz@nonmonotonic.com Zimmermann_edward 1.B are all words */
# define _IS_DOT_IN_WORD(_x) ((_x) == '.' || (_x) == '_' || (_x) == '&' || (_x) == '@')
#elif (DOT_WORDS_SIGNATURE == 3)
  /* As above but edz@nonmonotonic.com is edz and nonmonotonic.com terms. edz@nonmonotonic.com via literal */
# define _IS_DOT_IN_WORD(_x) ((_x) == '.' || (_x) == '_' || (_x) == '&')
#elif (DOT_WORDS_SIGNATURE == 2)
  /* Just Zimmermann_Edward as term */
# define _IS_DOT_IN_WORD(_x) ((_x) == '.' || (_x) == '_')
#elif (DOT_WORDS_SIGNATURE == 1)
  /* Zimmermann_Edward as Zimmermann Edward and Zimmermann_Edward via literal */
# define _IS_DOT_IN_WORD(_x) ((_x) == '.'))
#endif
  return _IS_DOT_IN_WORD(ch);
}
#endif

